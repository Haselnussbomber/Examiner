local frame = CreateFrame("Frame", "ExaminaNPCDataScanner")
frame.initialized = false;
frame:RegisterEvent("INSTANCE_ENCOUNTER_ENGAGE_UNIT");
frame:SetScript("OnEvent", function(event, arg1)
	if not frame.initialized then
		ExaminerNPCIDTable = ExaminerNPCIDTable or {};
		frame.initialized = true;
	end

	local found = false;

	for i = 1, MAX_BOSS_FRAMES do
		local key = "boss" .. i;
		if (UnitExists(key)) then
			local guid = UnitGUID(key);
			local npcID = guid and tonumber(guid:match("-(%d+)-%x+$"), 10) or nil;
			if (npcID and not ExaminerNPCData[npcID]) then
				found = true;
				if (not ExaminerNPCIDTable[npcID]) then
					print(("[Examiner] Boss %d: %s - %d"):format(i, UnitName(key), npcID));
					ExaminerNPCIDTable[npcID] = UnitName(key);
				end
			end
		end
	end

	if (not found) then
		print("[Examiner] Missing NPC ID!")
	end
end)


